public class Println{
    public static void main(StringTest[] args) {
        // println
        System.out.println("println で出力");
        System.out.println("01234");
        System.out.println("56789");

        // print
        System.out.println("print で出力");
        System.out.print("01234");
        System.out.print("56789\n");

        // 数値を出力
        System.out.println("数値を出力");
        System.out.println(1234);
        System.out.println(56789);
    }
}